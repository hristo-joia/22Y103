#include <stdio.h>

// Άσκηση 1

int main()
{
    double a, b, c; // πραγματικές μεταβλητές για τις πλευρές του τριγώνου
    printf("Δώσε τα μήκη των 3 πλευρών τριγώνου: ");
    scanf("%lf%lf%lf", &a, &b, &c);

    // πρέπει να ισχύουν ταυτόχρονα οι ανισότητες
    if ((a + b > c) && (a + c > b) && (b + c > a))
        printf("Αυτό το τρίγωνο υπάρχει.");
    else
        printf("Δεν υπάρχει τέτοιο τρίγωνο.");
    return 0;
}
