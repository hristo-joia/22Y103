#include <stdio.h>
// Άσκηση 1

int main() {
    for (int i = 1; i <= 100; i++) {
        // Εκτύπωσε τον αριθμό, ακολουθούμενο από έναν οριζόντιο στηλοθέτη
        printf("%d\t", i);

        // Αν ο αριθμός είναι πολλαπλάσιο του 10 πήγαινε στην επόμενη γραμμή
        if (i % 10 == 0) printf("\b\n");
        // \b για να σβηστεί ο τελευταίος στηλοθέτης
    }
    return 0;
}
